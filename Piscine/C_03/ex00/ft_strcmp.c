int	ft_strcmp(char *s1, char *s2)
{
	unsigned char	*a1;
	unsigned char	*a2;

	a1 = (unsigned char *)s1;
	a2 = (unsigned char *)s2;
	while (*a1 && (*a1 == *a2))
	{
		a1++;
		a2++;
	}
	return (*a1 - *a2);
}
